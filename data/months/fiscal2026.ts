const odiaDigits = ["୦", "୧", "୨", "୩", "୪", "୫", "୬", "୭", "୮", "୯"];

const toOdiaNumber = (value: number) =>
  value
    .toString()
    .padStart(2, "0")
    .split("")
    .map((digit) => odiaDigits[Number(digit)])
    .join("");

const createHoliday = (
  year: number,
  month: number,
  day: number,
  festival: string,
  colorIndex: number
): HolidayType => ({
  date: toOdiaNumber(day),
  englishDate: day,
  isoDate: `${year}-${String(month).padStart(2, "0")}-${String(day).padStart(
    2,
    "0"
  )}T00:00:00Z`,
  festival,
  color: holidayColors[colorIndex % holidayColors.length],
});

const createMonthOverride = (
  holidays: HolidayType[],
  subhadina: SubhadinaType,
  festival?: MonthData["festival"],
  headerLine?: MonthData["headerLine"]
): Partial<MonthData> => ({
  holidays,
  subhadina,
  ...(festival ? { festival } : {}),
  ...(headerLine ? { headerLine } : {}),
  dateDetails: [],
  moonIcons: {},
});

